                    <h4>Adatok: </h4>

                    <ul class="list-group mt-2 mb-4">
                        <li class="list-group-item">2.6 L (2,568 cc), bore x stroke: 86 mm × 73.7 mm (3.39 in × 2.90 in)</li>
