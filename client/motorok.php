                    <p>3 éve a Nissan elkezdett hozzájuk újra alkatrészeket gyártani (blokk, hengerfej stb.).</p>

                    <h4 class="mt-4">RB26 problémái:</h4>
                    <p class="text-justify">A kezdetleges RB26-ok amik az R32 GTR-ben voltak oljazási gondokkal küzdöttek. A főtengelyen az olajpumpa meghajtó felületét kicsire munkálták meg ez végett magasfordulaton képes volt "átfordulni"
                    a pumpa, így megszűnve az olajnyomás. A későbbi szériában a Nissan javította a hibát, de rengeteg utángyártó cég is kifejlesztett egy megoldást rá. Az egyik például a Toyota 2JZ megoldását használta fel.</p>

                    <p class="text-justify">Következő probléma, hogy az egyik hengerfej csavar és az egyik olajfurat közel van egymáshoz, és ez végett kb. 1000LE felett hajlamos "ketté" törni blokk.
                     Legjobb megoldás rá, ha RB30-at építünk meg 1000LE fölé mert ott a blokk magasabb így a csavar is toválabb esik a furattól.   
                    </p>

                    <p class="text-justify">Egyik ismert problémája még a hengerfejbe menő sok olaj. A blokk és a hengerfej között van egy fúvóka aminek az átmérője picit nagy, így ezáltal magasabb fordulaton képes egyszerre sok olaj a hengerfejbe szökni.
                        Egyik megoldás, hogy egy kisebb átméré fúvókára cseréljük, de alacsony fordulaton kevés olaj jut a hengerfejbe.
                        Legjobb megoldás, ha a hengerfej hátuljába egy olajleeresztőt teszünk amit a kartelba visszavezetünk, így arányosan fog nőni/csökkenni az olaj mennyisége a hengerfejben.
                    </p>

                    <h4 class="mt-4">RB26 és tuning</h4>

                    <p class="text-justify">
                        Az RB26 kiválóan alkalmas tuning célokra, köszönhetően a túlméretes blokknak (és annak öntvény anyagának), a kalodának (a főtengely lefogató csészék egybe vannak) és sok más dolognak.
                        A Nissan önmaga is adott ki gyárilag épített motorokat (RB-X GT2, N1, Z2).  
                    </p>

                    <h5 class="mt-2">RB-X GT2</h5>

                    <p class="text-jusitfy">A REINIK álltal fejelsztett és épített motor amit a Nissan Skyline Nismo 400R-ben kapott helyett. A furatát 87mm-re növelték és a löketét meg 77.7 mm-re. Erősített blokk és hengerfej,
                        fém hengerfej tömítés, dugattyúk hűtő csatornákkal, erősített hajtokarok és az N1 blokk turbóját kapta meg. 444 lóerőt teljesített. Idézőjeles RB28.
                    </p>

                    <h5 class="mt-2">N1</h5>

                    <p class="text-justify">
                        A sima RB26 továbbfejlesztett változata amit a NISMO a Group A és a Group N versenyekhez készített. Sokkal kiegyensúlyozottab főtengely (így akár 7200 fölé is mehet a motor fordulatszáma), javított víz és olaj járatok a blokkban, és továbbfejlesztett vezérműtengelyek és egy turbó feltöltő.
                        A turbó feltöltő maradt ugyanaz mint a sima RB26-nál annyi különbséggel, hogy a lapátkerekek acélből készültek a kerámia helyett.
                        A blokk akár 88mm-ig fúrható. A két blokk könnyen megkülömböztethető egymástól, az RB26 jelölése 05U míg az N1-é 24U (a jelölés a blokkban van benne).
                    </p>

                    <h5 class="mt-2">Z2</h5>

                    <p>
                        A Nissan Skyline GT-R Z-Tune motorja. Erősített GT-500-as blokk az alapja, és a motor térfogatát 2.8 literre növelték.
                    </p>

                    <p class="text-justify">
                        Persze nem muszály ezeket a blokkokat beszereznünk, bőven rendelkezésre állnak utángyártott alkatrészek amik kifezetten magas loerőre vannak kifejlesztve. Sok neves gyártó,
                        köztük a Tomei, HKS, CP, Mahley, JE, Wiseco, Carrillo és az Eagle készít hozzá kiváló minőségű alkatrészeket.
                        De induljunk fentről lefelé.
                    </p>

                    <h5 class="mt-2">Hengerfej</h5>

                    <p class="text-justify">
                        Legelsőnek nagyon fontos leszögezni, hogy az RB26 és az RB25 hengerfej nem ugyanaz! Hiába 100 köbcenti különbség vannak rajtuk különbségek. Legelső, hogy a hengerfej csavarok nem egyforma méretűek (RB26: 11mm, RB25:10mm).
                        Második az RB25 hengerfeje hidrótőkés és VCT (Variable Cam Timing) van benne, ennek köszönhetően a blokkon plusz egy olajfurat van így az végett a mellette elhelyezkedő víz furat se ugyanaz.
                        Fontos, amelyik alkatrésznél oda van írva, hogy RB26/25, győzödjünk meg arról, hogy mindkét blokkon az az alaktrész ugyanaz e!
                    </p>
                    
                    <p class="text-justify">
                        A vezétműtengely egy fontos része a motornak, hisz ez vezérli a szelepek nyitását/zárását. Sokat számít a tengely nyitása magassága, szögelése stb. 
                        Itt áruházunkban sok féle tengelyt árulunk ehhez a motorhoz, de egy két dolgot fontos leszögezni.
                        A motor úgynevezett CAS szenzort használ ami a kipufogó oldali tengelyről veszi a jelet, így tudja az ECU, hogy a főtengely melyik pozicióban van éppen.
                        Az R32 és R33-ban ez a szenzor ugyanaz de az R34-nél már változtattak rajta. 
                    </p>
                    
                    <p>Type 1 (R32, R33) : Aluminium ház, és a tengely belsején egy hatszögletű lapoláson keresztül hajtja a vezérműtengely.</p>
                    <p>Type 2 (R34): Aluminium és műanyag ház, és a tengely végén egy bevágás van ami a vezérműtengelyben lévő ék hajtja.</p>
                    
                    <p>(Az áruházban a tengelyekhez odavan írva, hogy melyik szenzorhoz valók.)</p>

                    <p class="text-justify">
                        Fontos figyelni a tengely szelep nyitás nagyságát, mert egy bizonyos méret felett a gyári hengerfejben a tengely nem fordul el. A gyári hengerfejben maximum 9.35mm nyitású tengely tud elfordulni,
                        az felett már portolni kell a fejet azaz ki kell képezni a bütyöknek az extra helyett.
                        Fontos még, ahogy növekszik a szelepnyitás mélysége, úgy csökken a tengely "alapköre", így ez végett magasabb szelepemelőket kell berakni.
                    </p>

                    <p class="text-justify">
                        Szeleprúgók is egy olyan tényezők amiket nem szabad figyelmen kívül hagyni. Feladatuk nagyon egyszerű, a szelepet zárva kell tudniuk tartani. Fontos, hogy kellő figyelmet fordítsunk rájuk,
                        hisz akkor is helyt kell állniuk ha mondjuk a motor forog 8700-at vagy mondjuk megvan terhelve egy 3.5 bar töltőnyomással (előfordulhat, hogy akkora nyomás, hogy nemes egyszerűséggel belöki a szelepet).
                    </p>

